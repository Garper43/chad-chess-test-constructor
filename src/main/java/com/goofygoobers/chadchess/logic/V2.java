package com.goofygoobers.chadchess.logic;

public class V2 {
    private int x;
    private int y;

    public V2(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public V2() {

    }

    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }
    public void setY(int y) {
        this.y = y;
    }
}
